const numeros = [1, 2, 3, 4, 5];

numeros.forEach(function(numero, index) {
    console.log(`O número ${numero} está na posição ${index}`);
});

// Saída:
// O número 1 está na posição 0
// O número 2 está na posição 1
// O número 3 está na posição 2
// O número 4 está na posição 3
// O número 5 está na posição 4