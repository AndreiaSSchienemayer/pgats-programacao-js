/*
CONDICIONAIS COM SWITCH e CASE
Usado quando tem mais de 3 condicionais

escolha (condicao)
    se x: faca
    se y: faca
    se z: faca
 
*/
const peso=20
switch (peso){
    case 10:
        console.log('P')
        break
    case 20:
        console.log('M')
        break
    case 30:
        console.log('G')
        break
    default://else
        console.log('ND')
}